/*change background colour*/
function changeMode() {
    var valueMode = document.getElementById("selectMode").value;
    if (valueMode == "white") {
        document.getElementById("bodyId").style.backgroundColor = "white";
        document.getElementById("selectMode").style.backgroundColor = "white";
        document.getElementById("selectTextMode").style.backgroundColor = "white";
    }
    else if (valueMode == "dodgerblue") {
        document.getElementById("bodyId").style.backgroundColor = "dodgerblue";
        document.getElementById("selectMode").style.backgroundColor = "dodgerblue";
        document.getElementById("selectTextMode").style.backgroundColor = "dodgerblue";
    }
    else if (valueMode == "greenyellow") {
        document.getElementById("bodyId").style.backgroundColor = "greenyellow";
        document.getElementById("selectMode").style.backgroundColor = "greenyellow";
        document.getElementById("selectTextMode").style.backgroundColor = "greenyellow";
    }
    else if (valueMode == "deeppink") {
        document.getElementById("bodyId").style.backgroundColor = "deeppink";
        document.getElementById("selectMode").style.backgroundColor = "deeppink";
        document.getElementById("selectTextMode").style.backgroundColor = "deeppink";
    }
    else {
        document.getElementById("bodyId").style.backgroundColor = "darkviolet";
        document.getElementById("selectMode").style.backgroundColor = "darkviolet";
        document.getElementById("selectTextMode").style.backgroundColor = "darkviolet";
    }
}

